                <h4> Algorithm for the student Physical examination of a patient with vascular disease of the lower
                    extremities (clinical examination)</h4>

                <p> 1.The student is acquainted with the task</p>

                <p>2.Student determines pulsation of arteries of lower extremities at different levels (a.femoralis,
                    a.poplitea, a.tibialis posterior, a. dorsalis pedis).</p>

                <p>3.The student conducts the Laniel-Lavestin test with commentary: press a on the symmetrical parts of
                    the fingers of both extremities, to evaluate the condition of the &laquo;white spot&raquo;</p>

                <p>4.The student conducts the Opel test with commentary: lift up the lower limbs at an angle of 45
                    &deg;, hold the lower limbs in such a position for 1 min, evaluate the condition of the skin of both
                    feet</p>

                <p>5.Answer the questions in the form</p>

                <h4> Algorithm for the student Physical examination of a patient with vascular disease of the lower
                    extremities (varicose diseases, clinical examination)</h4>

                <p> 1.The student is acquainted with the task</p>

                <p>2.Hakkenbruch test with commentary: In the patient's upright position, the fingers of the doctor are
                    placed on the area of the sapheno-femoral anastomosis the investigated limb, below the inguinal
                    fold,. The patient produces 2-3 cough impulses. If there are insufficiency of the superficial vein
                    valves, the doctor during palpation determines the impulse of blood movement along the superficial
                    veins.</p>

                <p>3.Troynov-Trandelenburg test: The patient is in a lying position with a raised leg. Conduction of
                    compression of a large subcutaneous vein in the area of the inguinal fold. The patient moves into
                    standing position. Rapid swelling of varicose veins after cessation of compression indicates a lack
                    of communicable veins</p>

                <p>4.Answer the questions in the form</p>

